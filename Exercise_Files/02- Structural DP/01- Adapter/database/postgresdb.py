class PostgreSQLDatabase:
    def postgresql_insert(self, data):
        print(f"Inserting data {data} into PostgreSQL database")

    def postgresql_delete(self, data):
        print(f"Deleting data {data} from PostgreSQL database")

    def postgresql_update(self, data):
        print(f"Updating data {data} in PostgreSQL database")

    def postgresql_select(self, data):
        print(f"Selecting data {data} from PostgreSQL database")